    public void ordemDescrecente(){
        
         for(int linha = 0; linha < array_ordemDecrescente.size();linha++){
            for(int coluna = 0; coluna < array_ordemDecrescente.size() - 1;coluna++){
                if(array_ordemDecrescente.get(coluna) < array_ordemDecrescente.get(coluna+1)){
                    int guardaValor = array_ordemDecrescente.get(coluna);
                    array_ordemDecrescente.set(coluna,array_ordemDecrescente.get(coluna+1));
                    array_ordemDecrescente.set(coluna+1, guardaValor);
                }
            }
        }
        
        list_ordemDecrescente.removeAll();
        
        for(int i = 0;i < array_ordemDecrescente.size();i++){
            list_ordemDecrescente.add(String.valueOf(array_ordemDecrescente.get(i)));
        }
    }
    
    public void ordemCrescente(){
        
        for(int linha = 0; linha < array_ordemCrescente.size();linha++){
            for(int coluna = 0; coluna < array_ordemCrescente.size() - 1;coluna++){
                if(array_ordemCrescente.get(coluna) > array_ordemCrescente.get(coluna+1)){
                    int guardaValor = array_ordemCrescente.get(coluna);
                    array_ordemCrescente.set(coluna,array_ordemCrescente.get(coluna+1));
                    array_ordemCrescente.set(coluna+1, guardaValor);
                }
            }
        }
        
        list_ordemCrescente.removeAll();
        
        for(int i = 0;i < array_ordemCrescente.size();i++){
            list_ordemCrescente.add(String.valueOf(array_ordemCrescente.get(i)));
        }
        
    }
    
    private void jTextField_numeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField_numeroActionPerformed

        int numero = Integer.parseInt(jTextField_numero.getText());
        
        array_ordemCrescente.add(numero);
        
        array_ordemDecrescente.add(numero);
        
        if(array_ordemCrescente.size() > 1 && array_ordemDecrescente.size() > 1){
            ordemCrescente();
            ordemDescrecente();
        }else{
            list_ordemCrescente.add(String.valueOf(array_ordemCrescente.get(0)));
            list_ordemDecrescente.add(String.valueOf(array_ordemDecrescente.get(0)));
        }
        
        limparEntrada();

    }//GEN-LAST:event_jTextField_numeroActionPerformed

    public void limparEntrada(){
        jTextField_numero.setText("");
    }
    
